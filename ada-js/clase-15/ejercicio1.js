// Ejercicio 1: Añadir personajes a una lista

// Lista inicial de personajes
let personajes = ["Link", "Zelda"];

// Agregamos "Mario" y "Luigi" al final de la lista usando el método push
personajes.push("Mario");
personajes.push("Luigi");

// Mostramos el array actualizado
console.log(personajes);
// Resultado: ["Link", "Zelda", "Mario", "Luigi"]
