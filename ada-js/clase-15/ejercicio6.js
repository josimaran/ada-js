// Ejercicio 6: Buscar la última posición de un elemento repetido

// Lista de números
let numeros = [2, 5, 7, 2, 8, 7];

// Encontramos la última posición del número 7
let ultimaPosicion = numeros.lastIndexOf(7);

// Mostramos la última posición encontrada
console.log("Última posición de '7':", ultimaPosicion);
// Resultado: 5
