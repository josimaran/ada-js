// Defino distintos tipos de datos
let numero = 18; // Numero entero
let numero2 = 18.23; // Numero decimal
let texto = "Hola universo"; // es un string puede ser con comillas dobles o simples
let booleano = false; // También puede ser true
let sindefinir; // Indefinido (sin asignar valor)
let nulo = null; // Valor vacío

console.log(`Numeros: ${numero} y ${numero2}`);
console.log(`Numero: ${numero}`);
console.log(`Texto: ${texto}`);
console.log(`Booleano: ${booleano}`);
console.log(`Sin definir: ${sindefinir}`); // undefined implícito
console.log(`Nulo: ${nulo}`); // null

